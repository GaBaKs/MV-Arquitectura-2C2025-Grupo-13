Inicio: MOV [4],0
	    MOV EDX,DS
	    LDH ECX,4
        LDL ECX,1
	    MOV EAX,1
	    SYS 1 		;LEO EL DATO
	    CMP [EDX],0 	;COMPRUEBO SI ES NEGATIVO
	    JN Fin 		;SI ES NEGATIVO TERMINO
Otro:   CMP [EDX],0 	;COMPRUEBO DE VUELTA A VER SI ES 0
        JZ Cont 	;SI ES 0 IMPRIMO LA CANTIDAD DE 1s Y REINICIO
        OR EBX,[EDX] 	;COPIO EN EBX EL CONTENIDO DE LA CELDA APUNTADA POR EDX
        DIV EBX,2 	;DIVIDO EBX POR 2
        CMP AC,0 	;COMPRUEBO SI ES PAR O IMPAR
        JZ Sigue 	;SI ES PAR ME SALTEO EL CONTADOR
        ADD [4],1 	;SI ES IMPAR SUMO 1
Sigue:  SHR [EDX],1 	;AVANZO EN EL NUMERO SHIFTEANDO A DERECHA
        JMP Otro 	;VUELVO A ANALIZAR EL NUMERO HASTA QUE SEA 0 POR SHIFTEAR EL NUMERO
Cont:   MOV EDX,DS
        LDH ECX,4
        LDL ECX,1
        MOV EAX,1
        ADD EDX,4
        SYS 2 		;IMPRIMO LA CANTIDAD DE 1s
Fin:    STOP 		;TERMINO	